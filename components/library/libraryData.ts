// ── Helper to resolve curated rows into book arrays ──
export function resolveCuratedRow(
  row: CuratedRow,
  books: LibraryBook[]
): LibraryBook[] {
  const bookMap = new Map(books.map((b) => [b.id, b]));
  return row.bookIds
    .map((id) => bookMap.get(id))
    .filter((b): b is LibraryBook => b !== undefined);
}
